echo "         • ADMIN_NOTIFY_ALLOWED_ORIGINS = https://anuragverma-author.vercel.app"
echo ""
echo "[ ] 6. Get function URL"
echo "       - Go to Supabase Project Settings → Edge Functions"
echo "       - Copy the admin-notify function URL"
echo ""
echo "[ ] 7. Set Vercel environment variable"
echo "       - Go to Vercel → Project Settings → Environment Variables"
echo "       - Add: VITE_ADMIN_NOTIFICATION_ENDPOINT=<function-url>"
echo ""
echo "[ ] 8. Redeploy production"
echo "       - npm run deploy"
echo ""
echo "[ ] 9. Test the forms"
echo "       - Submit a contact form"
