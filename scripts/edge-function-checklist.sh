echo "📋 Supabase Edge Function Deployment Checklist"
echo "=============================================="
echo ""
echo "Before deploying the admin-notify Edge Function, ensure:"
echo ""
echo "[ ] 1. Supabase project created"
echo "       - Sign up at https://supabase.com"
echo "       - Create a new project"
echo ""
echo "[ ] 2. Supabase CLI installed"
echo "       - npm install -g supabase"
echo "       - supabase login"
echo ""
echo "[ ] 3. Resend account created"
echo "       - Sign up at https://resend.com"
echo "       - Create API key at https://resend.com/api-keys"
echo "       - Copy and save your API key"
echo ""
echo "[ ] 4. Deploy Edge Function"
echo "       - Run: supabase functions deploy admin-notify --project-id <project-id>"
echo ""
echo "[ ] 5. Set Supabase secrets"
echo "       - Go to Supabase dashboard → Project Settings → Edge Functions"
echo "       - Set these secrets:"
echo "         • RESEND_API_KEY = (your API key from Resend)"
